class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;

  late WebViewController webViewController;

  late JavascriptChannel mainJavascriptChannel;

  Set<JavascriptChannel> channelList = Set<JavascriptChannel>();

  void _incrementCounter() {
    setState(() {
      // This call to setState tells the Flutter framework that something has
      // changed in this State, which causes it to rerun the build method below
      // so that the display can reflect the updated values. If we changed
      // _counter without calling setState(), then the build method would not be
      // called again, and so nothing would appear to happen.
      _counter++;
    });
  }

  @override
  void initState() {
    mainJavascriptChannel = new JavascriptChannel(
        name: "wecango",
        onMessageReceived: (message) async {
          print(message.message);
          var token = await FirebaseMessaging.instance.getToken();
          print("token");
          print(token);
        });

    channelList.add(mainJavascriptChannel);
    _requestPermissions();

    init();
  }

  void _requestPermissions() {
    flutterLocalNotificationsPlugin
        .resolvePlatformSpecificImplementation<
            IOSFlutterLocalNotificationsPlugin>()
        ?.requestPermissions(
          alert: true,
          badge: true,
          sound: true,
        );
  }

  void _configureDidReceiveLocalNotificationSubject() {}

  init() async {
    FirebaseMessaging messaging = FirebaseMessaging.instance;

    NotificationSettings settings = await messaging.requestPermission(
      alert: true,
      announcement: false,
      badge: true,
      carPlay: false,
      criticalAlert: false,
      provisional: false,
      sound: true,
    );

    FirebaseMessaging.onMessage.listen((event) async {
      const AndroidNotificationDetails androidPlatformChannelSpecifics =
          AndroidNotificationDetails(
              'testIdChannel', 'testChannel', ' description',
              importance: Importance.max,
              priority: Priority.high,
              showWhen: false);
      const NotificationDetails platformChannelSpecifics =
          NotificationDetails(android: androidPlatformChannelSpecifics);
      await flutterLocalNotificationsPlugin.show(
          0, 'plain title', 'plain body', platformChannelSpecifics,
          payload: 'item x');
    });
  }

  @override
  Widget build(BuildContext context) {
    // This method is rerun every time setState is called, for instance as done
    // by the _incrementCounter method above.
    //
    // The Flutter framework has been optimized to make rerunning build methods
    // fast, so that you can just rebuild anything that needs updating rather
    // than having to individually change instances of widgets.
    return Scaffold(
      body: SafeArea(
        child: Column(
          children: [
            Expanded(
                child: Container(
              child: WebView(
                initialUrl: "http://10.0.2.2:8080",
                javascriptMode: JavascriptMode.unrestricted,
                javascriptChannels: channelList,
                onWebViewCreated: onWebViewCreated,
                userAgent:
                    "Mozilla/5.0 (Linux; Android 10; Android SDK built for x86 Build/QSR1.190920.001; wv) AppleWebKit/537.36 (KHTML, like Gecko) Version/4.0 Chrome/74.0.3729.185 Mobile Safari/537.36 wecango",
              ),
            ))
          ],
        ),
      ),
    );
  }

  onWebViewCreated(WebViewController controller) {
    webViewController = controller;
  }
}
